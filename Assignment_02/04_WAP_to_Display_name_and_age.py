# Write a program to take name and age as input from the user and display it as.
# Hello <name>, you will turn <age + 1> next year.

# Taking name and age as input from the user

name = str(input("Enter your name: "))                          # Enter your name: Sahil Patil
age = int(input("Enter your age: "))                            # Enter your age: 24

# Displaying the result
print(f"Hello {name}, you will turn {age + 1} next year.")     # Hello Sahil Patil, you will turn 25 next year.